            const commTierB0 = communityTiers.filter(t => t.tier_number === 8)
            const commTierB1 = communityTiers.filter(t => t.tier_number === 7)
            const commTierB2 = communityTiers.filter(t => t.tier_number === 6)
            const commTierB3 = communityTiers.filter(t => t.tier_number === 5)
            const commTierA0 = communityTiers.filter(t => t.tier_number === 4)
            const commTierA1 = communityTiers.filter(t => t.tier_number === 3)
            const commTierA2 = communityTiers.filter(t => t.tier_number === 2)
            const commTierA3 = communityTiers.filter(t => t.tier_number === 1)

            const custodyTiers = workload.filter(w => w.location === 'CUSTODY')
            const cusUntiered = custodyTiers.filter(t => t.tier_number === 0)
            const cusTierD0 = custodyTiers.filter(t => t.tier_number === 16)
            const cusTierD1 = custodyTiers.filter(t => t.tier_number === 15)
            const cusTierD2 = custodyTiers.filter(t => t.tier_number === 14)
            const cusTierD3 = custodyTiers.filter(t => t.tier_number === 13)
            const cusTierC0 = custodyTiers.filter(t => t.tier_number === 12)
            const cusTierC1 = custodyTiers.filter(t => t.tier_number === 11)
            const cusTierC2 = custodyTiers.filter(t => t.tier_number === 10)
            const cusTierC3 = custodyTiers.filter(t => t.tier_number === 9)
            const cusTierB0 = custodyTiers.filter(t => t.tier_number === 8)
            const cusTierB1 = custodyTiers.filter(t => t.tier_number === 7)
            const cusTierB2 = custodyTiers.filter(t => t.tier_number === 6)
            const cusTierB3 = custodyTiers.filter(t => t.tier_number === 5)
            const cusTierA0 = custodyTiers.filter(t => t.tier_number === 4)
            const cusTierA1 = custodyTiers.filter(t => t.tier_number === 3)
            const cusTierA2 = custodyTiers.filter(t => t.tier_number === 2)
            const cusTierA3 = custodyTiers.filter(t => t.tier_number === 1)

            const licenseTiers = workload.filter(w => w.location === 'LICENSE')
            const licUntiered = licenseTiers.filter(t => t.tier_number === 0)
            const licTierD0 = licenseTiers.filter(t => t.tier_number === 16)
            const licTierD1 = licenseTiers.filter(t => t.tier_number === 15)
            const licTierD2 = licenseTiers.filter(t => t.tier_number === 14)
            const licTierD3 = licenseTiers.filter(t => t.tier_number === 13)
            const licTierC0 = licenseTiers.filter(t => t.tier_number === 12)
            const licTierC1 = licenseTiers.filter(t => t.tier_number === 11)
            const licTierC2 = licenseTiers.filter(t => t.tier_number === 10)
