            const licTierA1 = licenseTiers.filter(t => t.tier_number === 3)
            const licTierA2 = licenseTiers.filter(t => t.tier_number === 2)
            const licTierA3 = licenseTiers.filter(t => t.tier_number === 1)

            expect(communityTiers[0].forename, 'Forename should equal Anonymised').to.equal('Anonymised')
            expect(commUntiered[0].total_cases, 'Comm Untiered Cases should equal 0').to.equal(0)
            expect(commTierD0[0].total_cases, 'Comm D0 Cases should equal 0').to.equal(0)
            expect(commTierD1[0].total_cases, 'Comm D1 Cases should equal 0').to.equal(0)
            expect(commTierD2[0].total_cases, 'Comm D2 Cases should equal 0').to.equal(0)
            expect(commTierD3[0].total_cases, 'Comm D3 Cases should equal 0').to.equal(0)
            expect(commTierC0[0].total_cases, 'Comm C0 Cases should equal 0').to.equal(0)
            expect(commTierC1[0].total_cases, 'Comm C1 Cases should equal 0').to.equal(0)
            expect(commTierC2[0].total_cases, 'Comm C2 Cases should equal 0').to.equal(0)
            expect(commTierC3[0].total_cases, 'Comm C3 Cases should equal 0').to.equal(0)
            expect(commTierB0[0].total_cases, 'Comm B0 Cases should equal 0').to.equal(0)
            expect(commTierB1[0].total_cases, 'Comm B1 Cases should equal 0').to.equal(0)
            expect(commTierB2[0].total_cases, 'Comm B2 Cases should equal 0').to.equal(0)
            expect(commTierB3[0].total_cases, 'Comm B3 Cases should equal 0').to.equal(0)
            expect(commTierA0[0].total_cases, 'Comm A0 Cases should equal 0').to.equal(0)
            expect(commTierA1[0].total_cases, 'Comm A1 Cases should equal 0').to.equal(0)
            expect(commTierA2[0].total_cases, 'Comm A2 Cases should equal 0').to.equal(0)
            expect(commTierA3[0].total_cases, 'Comm A3 Cases should equal 0').to.equal(0)

            expect(cusUntiered[0].total_cases, 'Cus Untiered Cases should equal 0').to.equal(0)
            expect(cusTierD0[0].total_cases, 'Cus D0 Cases should equal 16').to.equal(16)
            expect(cusTierD1[0].total_cases, 'Cus D1 Cases should equal 15').to.equal(15)
            expect(cusTierD2[0].total_cases, 'Cus D2 Cases should equal 14').to.equal(14)
            expect(cusTierD3[0].total_cases, 'Cus D3 Cases should equal 13').to.equal(13)
            expect(cusTierC0[0].total_cases, 'Cus C0 Cases should equal 12').to.equal(12)
            expect(cusTierC1[0].total_cases, 'Cus C1 Cases should equal 11').to.equal(11)
            expect(cusTierC2[0].total_cases, 'Cus C2 Cases should equal 10').to.equal(10)
            expect(cusTierC3[0].total_cases, 'Cus C3 Cases should equal 9').to.equal(9)
